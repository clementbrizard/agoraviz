function showHideContrib() {
    var x = document.getElementById("lContrib");
    if (x.style.display == 'none')
    {
        x.style.display = 'block';
    }else
    {
        x.style.display = 'none';
    }
}

function showHideSynth() {
    var x = document.getElementById("lSynth");
    if (x.style.display == 'none')
    {
        x.style.display = 'block';
    }else
    {
        x.style.display = 'none';
    }
}